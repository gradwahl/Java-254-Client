package jagex2.dash3d;

import deob.ObfuscatedName;

@ObfuscatedName("p")
public class QuickGround {

	@ObfuscatedName("p.a")
	public int swColour;

	@ObfuscatedName("p.b")
	public int seColour;

	@ObfuscatedName("p.c")
	public int neColour;

	@ObfuscatedName("p.d")
	public int nwColour;

	@ObfuscatedName("p.e")
	public int textureId;

	@ObfuscatedName("p.f")
	public boolean flat = true;

	@ObfuscatedName("p.g")
	public int rgb;

	public QuickGround(int arg0, int arg1, int arg2, int arg3, int arg4, int arg5, boolean arg6) {
		this.swColour = arg0;
		this.seColour = arg1;
		this.neColour = arg2;
		this.nwColour = arg3;
		this.textureId = arg4;
		this.rgb = arg5;
		this.flat = arg6;
	}
}
